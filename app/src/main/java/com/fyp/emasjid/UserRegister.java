package com.fyp.emasjid;

public class UserRegister {

    public String name, gender, age, email, phone;

    public UserRegister() {
    }

    public UserRegister(String name, String gender, String age, String email, String phone) {
        this.name = name;
        this.gender = gender;
        this.age = age;
        this.email = email;
        this.phone = phone;
    }
}
